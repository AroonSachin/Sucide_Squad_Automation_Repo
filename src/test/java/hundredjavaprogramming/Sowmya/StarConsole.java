package hundredjavaprogramming.Sowmya;

public class StarConsole {

	public static void main(String[] args) {
		int number, row; 
		for(row=1; row<=10; row++)
		{
			for(number=1; number<=row; number++)
			{
				System.out.println("*");
			}
			System.out.println();
		}
		
		

	}

}
